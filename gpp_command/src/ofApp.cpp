
#include "ofApp.h"


void ofApp::setup() {
  inputHandler_.setButtonX(make_shared<JumpCommand>());
  inputHandler_.setButtonY(make_shared<FireGunCommand>());
}

void ofApp::update() {
  shared_ptr<Command> command = inputHandler_.handleInput();
  if (command) {
    command->execute(player_);
  }
}

